//Object in JavaScript
// Objects in JavaScript are used to store collections of data and more complex entities.
                // They are created using curly braces {} and consist of key-value pairs, 
                // where the key is a string (or symbol) and the value can be any data type, including other objects.

let person = {
    name: ["Jeeva","Kumar"],
    age: 25,
    city: "Chennai",
    profession: "Software Developer"
};

console.log(person.name[0]); // Output: Jeeva